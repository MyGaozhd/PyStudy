"""
运算符学习

"""

""" 算术运算符 """
a = 10
b = 11
# 加法
print(a + b)

# 减法
print(a - b)

# 乘法
print(a * b)

# 除法
print(a / b)

# 取模
print(a % b)

# 幂运算
print(a ** 5)

# 取整除
print(a // 3)

""" 比较运算符 """

a = "123"
b = "456"
c = "123"
# == 判断是否相等
print(a == b)
print(a == c)

# 判断不相等

if a != b:
    print("a != b")
